export function Sidebar() {
  const pathname = usePathname();
  const router   = useRouter();
  const { session, setSession } = useAuthStore();

  function isActive(item: typeof NAV[number]) {
    return item.exact ? pathname === item.href : pathname.startsWith(item.href);
  }

  async function handleLogout() {
    await supabase.auth.signOut();
    setSession(null);
    router.push("/login");
  }

  return (
    <aside
      className="glass-card flex flex-col py-6 px-3 h-full"
      aria-label="Navegación principal"
    >
      {/* Logo */}
      <div className="flex flex-col items-center mb-8 gap-1 px-2">
        <span className="font-mono text-xl text-gold tracking-widest font-bold">ARMY</span>
        <span className="text-[9px] tracking-[0.25em] text-[color:var(--text-muted)] uppercase">admin</span>
      </div>

      {/* Nav */}
      <nav className="flex flex-col gap-1 flex-1">
        {NAV.map((item) => {
          const active = isActive(item);
          return (
            <Link
              key={item.href}
              href={item.href}
              aria-current={active ? "page" : undefined}
              className="relative group"
            >
              {active && (
                <motion.div
                  layoutId="sidebar-indicator"
                  className="nav-active absolute inset-0 rounded-lg"
                  transition={{ type: "spring", stiffness: 500, damping: 35 }}
                />
              )}
              <span
                className={`relative flex items-center gap-3 px-4 py-2.5 rounded-lg transition-colors duration-200 ${
                  active
                    ? "text-white"
                    : "text-[color:var(--text-secondary)] hover:text-[color:var(--text-primary)] hover:bg-white/5"
                }`}
              >
                {item.icon}
                <span className="text-sm font-medium">{item.label}</span>
              </span>
            </Link>
          );
        })}
      </nav>

      {/* Auth */}
      <div className="pt-4 border-t border-[color:var(--glass-border)]">
        {session ? (
          <button
            type="button"
            onClick={handleLogout}
            className="flex w-full items-center gap-3 px-4 py-2.5 rounded-lg text-[color:var(--text-muted)] hover:text-red-400 hover:bg-red-500/10 transition-colors duration-200"
          >
            <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className="w-5 h-5">
              <path fillRule="evenodd" d="M7.5 3.75A1.5 1.5 0 0 0 6 5.25v13.5a1.5 1.5 0 0 0 1.5 1.5h6a1.5 1.5 0 0 0 1.5-1.5V15a.75.75 0 0 1 1.5 0v3.75a3 3 0 0 1-3 3h-6a3 3 0 0 1-3-3V5.25a3 3 0 0 1 3-3h6a3 3 0 0 1 3 3V9A.75.75 0 0 1 15 9V5.25a1.5 1.5 0 0 0-1.5-1.5h-6Zm10.72 4.72a.75.75 0 0 1 1.06 0l3 3a.75.75 0 0 1 0 1.06l-3 3a.75.75 0 1 1-1.06-1.06l1.72-1.72H9a.75.75 0 0 1 0-1.5h10.94l-1.72-1.72a.75.75 0 0 1 0-1.06Z" clipRule="evenodd" />
            </svg>
            <span className="text-sm font-medium">Salir</span>
          </button>
        ) : (
          <Link
            href="/login"
            className="btn-accent flex items-center justify-center gap-2 w-full text-sm"
          >
            <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className="w-4 h-4">
              <path fillRule="evenodd" d="M7.5 3.75A1.5 1.5 0 0 0 6 5.25v13.5a1.5 1.5 0 0 0 1.5 1.5h6a1.5 1.5 0 0 0 1.5-1.5V15a.75.75 0 0 1 1.5 0v3.75a3 3 0 0 1-3 3h-6a3 3 0 0 1-3-3V5.25a3 3 0 0 1 3-3h6a3 3 0 0 1 3 3V9A.75.75 0 0 1 15 9V5.25a1.5 1.5 0 0 0-1.5-1.5h-6Zm5.03 4.72a.75.75 0 0 0-1.06 0l-3 3a.75.75 0 0 0 0 1.06l3 3a.75.75 0 1 0 1.06-1.06l-1.72-1.72H15a.75.75 0 0 0 0-1.5H9.81l1.72-1.72a.75.75 0 0 0 0-1.06Z" clipRule="evenodd" />
            </svg>
            Ingresar
          </Link>
        )}
      </div>
    </aside>
  );
}
